from typing import List


class Solution:
    def shipWithinDays(self, weights: List[int], D: int) -> int:
        l, r = 1, 500 * 50000
        def can_do(weights, D, m):
            agg = 0
            i = 0
            cnt = 0
            while i < len(weights):
                cur = weights[i]
                if agg + cur > m:
                    agg = 0
                    cnt += 1
                elif agg + cur == m:
                    agg = 0
                    cnt += 1
                    i += 1
                else:
                    agg += cur
                    i += 1
                if cnt > D:
                    return False
            if 0 < agg <= m:
                cnt += 1
            return cnt <= D
        while l <= r:
            m = (l + r) // 2
            if can_do(weights, D, m):
                r = m - 1
            else:
                l = m + 1
        return l

